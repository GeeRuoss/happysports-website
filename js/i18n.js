function t(key) {
  const lang = window.HS_LANG;
  return (window.HS_I18N[lang] && window.HS_I18N[lang][key]) ||
         (window.HS_I18N['fr'] && window.HS_I18N['fr'][key]) || key;
}

function setLang(lang) {
  window.HS_LANG = lang;
  localStorage.setItem('hs_lang', lang);
  location.reload();
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val) el.innerHTML = val;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const val = t(key);
    if (val) el.placeholder = val;
  });
  // Update lang toggle buttons
  document.querySelectorAll('.lang-toggle button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === window.HS_LANG);
  });
}
